from statistics import multimode, mean, median

#Statisztika modul

#Módusz
print(f"A tömb értékeinek módusza: {multimode([1, 1, 14,20,3,12, 1, 17, 17,9, 17])}")

#Átlag
print(f"A tömb értékeinek átlaga: {mean([1, 1, 14,20,3,12, 1, 17, 17,9, 17])}")

#Medián
print(f"A tömb értékeinek mediánja: {median([1, 1, 14,20,3,12, 1, 17, 17,9, 17])}")
